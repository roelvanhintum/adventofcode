const data = input.map((line) => {
    const lineInput = line.split('');
    return lineInput.map((char) => {
        return char === '#';
    });
});

let trees = 0;

function nextStep(down, right, addDown, addRight) {
    if (data[down][right]) {
      trees++;
    }

    if (typeof data[down + addDown] !== 'undefined') {
        if (typeof data[down + addDown][right + addRight] !== 'undefined') {
          nextStep(
            down + addDown,
            right + addRight,
            addDown,
            addRight
          );
        } else {
          nextStep(
            down + addDown,
            (right + addRight) - data[0].length,
            addDown,
            addRight
          );
        }
    }
}

